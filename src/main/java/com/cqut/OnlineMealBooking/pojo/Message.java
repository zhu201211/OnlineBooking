package com.cqut.OnlineMealBooking.pojo;

/**
 * 消息实体
 * @author 祝剑锋
 *
 */
public class Message {
    private String msg;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    @Override
    public String toString() {
	return "[msg=" + msg + "]";
    }

   
    
}
